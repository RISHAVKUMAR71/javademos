package first_java01;

public class charandboolean {

	public static void main(String[] args) {
		char mychar = 'g';
		System.out.println(mychar);

		int k = mychar;
		System.out.println(k);
		
		char mychar2 = 80;	
		System.out.println(mychar2);
		
		char mynewchar2 = '\u0044';	
		System.out.println(mynewchar2);
		
		char mynewchar3 = '\uEE44';	
		System.out.println(mynewchar3);
		
		char mynewchar4 = '\u00A9';	
		System.out.println(mynewchar4);
		
		boolean flag = !true;
		System.out.println(flag);
	}

}
